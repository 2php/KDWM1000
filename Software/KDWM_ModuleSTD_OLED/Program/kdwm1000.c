/*====================================================================================================*/
void KDWM_Init( void )
{
  SystemInit();
  HAL_InitTick();

  KDWM_GPIO_Config();
  KDWM_SSD1331_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Loop( void )
{
  uint8_t i = 0;

  OLED_PutStr(CENTER(FONT8, "HELLO"), LINE(FONT8, 2), &FONT8, "HELLO", WHITE, BLACK);
  OLED_PutStr(CENTER(FONT8, "WORD"),  LINE(FONT8, 3), &FONT8, "WORD",  WHITE, BLACK);

  while(1) {
    OLED_PutNum(CENTER(FONT8, "   "), LINE(FONT8, 5), &FONT8, Type_D, 3, i++, WHITE, BLACK);
    LED_G_Toggle();
    delay_ms(200);
  }
}
