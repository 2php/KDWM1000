#include "drivers\stm32f4_system.h"
#include "modules\module_serial.h"
#include "modules\module_dw1000.h"

#include "kdwm1000.h"
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Init( void )
{
  SystemInit();
  HAL_InitTick();

  KDWM_GPIO_Config();
  KDWM_UART_Config(NULL);
  KDWM_DW1000_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Loop( void )
{
  while(1) {
    LED_G_Toggle();
    delay_ms(100);
  }
}
