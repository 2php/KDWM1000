#include "drivers\stm32f4_system.h"

#include "kdwm1000.h"
/*====================================================================================================*/
/*====================================================================================================*/
void KEY_extiEven( void )
{
  LED_R_Toggle();
}

void KDWM_Init( void )
{
  SystemInit();
  HAL_InitTick();

  KDWM_GPIO_Config();
  KDWM_EXTI9_5_Config(KEY_extiEven);
}
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Loop( void )
{
  while(1) {
    LED_G_Toggle();
    delay_ms(200);
  }
}
