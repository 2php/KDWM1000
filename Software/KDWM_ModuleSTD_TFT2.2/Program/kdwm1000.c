#include "drivers\stm32f4_system.h"

#include "kdwm1000.h"
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Init( void )
{
  SystemInit();
  HAL_InitTick();

  KDWM_GPIO_Config();
  KDWM_ILI9341_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Loop( void )
{
  while(1) {
    LED_R_Reset();
    LED_G_Set();
    LED_B_Set();
    delay_ms(200);
    LED_R_Set();
    LED_G_Reset();
    LED_B_Set();
    delay_ms(200);
    LED_R_Set();
    LED_G_Set();
    LED_B_Reset();
    delay_ms(200);
    while(KEY_Read()) {
      LED_R_Set();
      LED_G_Set();
      LED_B_Set();
      delay_ms(200);
      LED_R_Reset();
      LED_G_Reset();
      LED_B_Reset();
      delay_ms(200);
    }
  }
}
