#include "drivers\stm32f4_system.h"

#include "kdwm1000.h"
#include "kdwm1000_bsp.h"
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Init( void )
{
  SystemInit();
  HAL_InitTick();

  KDWM_GPIO_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void KDWM_Loop( void )
{
  while(1) {
    LED_R_Toggle();
    delay_ms(200);
    LED_G_Toggle();
    delay_ms(200);
    LED_B_Toggle();
    delay_ms(200);
    while(KEY_Read()) {
      LED_R_Toggle();
      LED_G_Toggle();
      LED_B_Toggle();
      delay_ms(200);
    }
  }
}
